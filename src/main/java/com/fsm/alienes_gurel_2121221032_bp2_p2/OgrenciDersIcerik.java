    private void tbl_tumDerslerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_tumDerslerMouseClicked
        int id = (int) tbl_dersler_model.getValueAt(tbl_tumDersler.getSelectedRow(), 0);
        Ders d = DB_Ders_Icerik_Takibi.searchDersById(id);
        btn_derseKatil.setEnabled(true);
        btn_derstenCik.setEnabled(false);
        if (d != null) {
            txt_dersId.setText(d.id + "");
            txt_dersId.setEnabled(false);
            txt_dersAdi.setText(d.ders_adi);
            txt_dersAdi.setEnabled(false);
            txt_dersAkts.setText(d.ders_akts + "");
            txt_dersAkts.setEnabled(false);
            txta_dersIcerigi.setText(d.icerik);
            txta_dersIcerigi.setEnabled(false);
        }
        Object value = tbl_dersler_model.getValueAt(tbl_tumDersler.getSelectedRow(), 0);
        boolean valueExists = false;
        for (int i = 0; i < tbl_ogrencıAldıgıDersler_model.getRowCount(); i++) {
            Object tableValue = tbl_ogrencıAldıgıDersler_model.getValueAt(i, 0);
            if (value.equals(tableValue)) {
                valueExists = true;
                break;
            }
        }
        if (valueExists) {
            btn_derseKatil.setEnabled(false);
        } else {
            btn_derseKatil.setEnabled(true);
        }
    }//GEN-LAST:event_tbl_tumDerslerMouseClicked

