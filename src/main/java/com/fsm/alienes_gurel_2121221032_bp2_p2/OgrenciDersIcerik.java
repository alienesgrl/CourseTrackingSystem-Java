    private void btn_derseKatilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_derseKatilActionPerformed
        if (tbl_tumDersler.getSelectedRow() < 0) {
            return;
        }
        int id_ders = Integer.parseInt(tbl_dersler_model.getValueAt(tbl_tumDersler.getSelectedRow(), 0).toString());
        String ders_adi = tbl_dersler_model.getValueAt(tbl_tumDersler.getSelectedRow(), 1).toString();
        int ders_akts = Integer.parseInt(tbl_dersler_model.getValueAt(tbl_tumDersler.getSelectedRow(), 2).toString());
        if (kullanici.kalan_akts >= ders_akts) {
            DB_Ders_Icerik_Takibi.AddCourseToStudentById(this.kullanici.id, id_ders, ders_adi, ders_akts);
            ArrayList<Ders> dersler = DB_Ders_Icerik_Takibi.GetStudentSelectedCoursesByIdJoin(this.kullanici.id);
            tbl_ogrencıAldıgıDersler_model.setRowCount(0);
            for (Ders ders : dersler) {
                tbl_ogrencıAldıgıDersler_model.addRow(new Object[]{ders.id, ders.ders_adi, ders.ders_akts});
            }
            txt_dersId.setText("");
            txt_dersAdi.setText("");
            txta_dersIcerigi.setText("");
            txt_dersAkts.setText("");
            btn_derseKatil.setEnabled(false);
            btn_derstenCik.setEnabled(false);
            guncelle();
        } else {
            JOptionPane.showMessageDialog(parentFrame, "Basarisiz");
        }
    }//GEN-LAST:event_btn_derseKatilActionPerformed

