    private void SetCourseToTable() {
        ArrayList<Ders> dersler = DB_Ders_Icerik_Takibi.DersleriGetir();
        tbl_dersler_model.setRowCount(0);
        for (Ders ders : dersler) {
            tbl_dersler_model.addRow(new Object[]{ders.id, ders.ders_adi, ders.ders_akts});
        }
    }

    private void OgrenciSetCourseToTable() {

        txt_ogrencıDersArama.setText(kullanici.id + "");
        txt_ogrencıDersArama.setEnabled(false);
        int search = kullanici.id;
        ArrayList<Ders> dersler = DB_Ders_Icerik_Takibi.aramaOgrenciDers(search);
        tbl_ogrencıAldıgıDersler_model.setRowCount(0);
        for (Ders ders : dersler) {
            tbl_ogrencıAldıgıDersler_model.addRow(new Object[]{ders.id, ders.ders_adi, ders.ders_akts});
        }
    }

