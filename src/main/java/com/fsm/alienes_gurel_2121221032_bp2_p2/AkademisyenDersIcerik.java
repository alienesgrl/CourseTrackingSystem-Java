    private void btn_kontrolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kontrolActionPerformed
        int ders_id = (int) tbl_dersler_model2.getValueAt(tbl_dersler2.getSelectedRow(), 0);
        int hafta = cmbx_haftaSec.getSelectedIndex();
        btn_secimiTemizle.setEnabled(true);
        DuyuruDisiniKapa(true);
        if (hafta != 0 && DB_Ders_Icerik_Takibi.IcerikVarMi(ders_id, hafta)) {
            btn_icerigiKaydet.setEnabled(false);
            btn_icerikGuncelle.setEnabled(true);
        } else if (hafta != 0 && !DB_Ders_Icerik_Takibi.IcerikVarMi(ders_id, hafta)) {
            btn_icerigiKaydet.setEnabled(true);
            btn_icerikGuncelle.setEnabled(false);
        } else {
            btn_icerigiKaydet.setEnabled(false);
            btn_icerikGuncelle.setEnabled(false);
        }
    }//GEN-LAST:event_btn_kontrolActionPerformed

    private void btn_icerikGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_icerikGuncelleActionPerformed
        Icerik i = new Icerik();
        int ders_id = (int) tbl_dersler_model2.getValueAt(tbl_dersler2.getSelectedRow(), 0);
        int hafta = cmbx_haftaSec.getSelectedIndex();
        i.ders_id = ders_id;
        i.haftasi = hafta;
        if (DB_Ders_Icerik_Takibi.IcerikVarMi(ders_id, hafta)) {
            if (rbtn_final.isSelected()) {
                i.eklenen = rbtn_final.getText();
            } else if (rbtn_vize.isSelected()) {
                i.eklenen = rbtn_vize.getText();
            }
            if (i.eklenen == null) {
                i.eklenen = "";
            }
            if (rbtn_quiz.isSelected()) {
                i.eklenen += "\n" + rbtn_quiz.getText();
            }
            if (rbtn_odev.isSelected()) {
                i.eklenen += "\n" + rbtn_odev.getText();
            }
            if (rbtn_pdf.isSelected()) {
                i.eklenen += "\n" + rbtn_pdf.getText();
            }
            if (rbtn_video.isSelected()) {
                i.eklenen += "\n" + rbtn_video.getText();
            }
            if (DB_Ders_Icerik_Takibi.UpdateIcerik(i)) {
                JOptionPane.showMessageDialog(parentFrame, "Basarili");
            } else {
                JOptionPane.showMessageDialog(parentFrame, "Basarisiz");
            }
        }
    }//GEN-LAST:event_btn_icerikGuncelleActionPerformed

