    private void SetKullaniciToTable() {
        ArrayList<Kullanici> kullanicilar = DB_Ders_Icerik_Takibi.OgrencileriGetir();
        tbl_kullanicilar_model.setRowCount(0);
        for (Kullanici kul : kullanicilar) {
            if (kul.kullanici_turu.equals("OGRENCI")) {
                tbl_kullanicilar_model.addRow(new Object[]{kul.id, kul.ad_soyad, kul.kullanici_turu, kul.kullanici_akts});
            }
        }
    }

